'use client';

import { firebaseConfig } from '@/firebase/config';
import { initializeApp, getApps, getApp, FirebaseApp } from 'firebase/app';
import { getAuth } from 'firebase/auth';
import { getFirestore } from 'firebase/firestore'

// IMPORTANT: DO NOT MODIFY THIS FUNCTION
export function initializeFirebase() {
  if (getApps().length > 0) {
    return getSdks(getApp());
  }

  // When deployed to Firebase App Hosting, environment variables are automatically
  // set. In a local development environment, we'll fall back to the config object.
  if (process.env.NEXT_PUBLIC_FIREBASE_PROJECT_ID) {
    // This is the ideal initialization for App Hosting.
    return getSdks(initializeApp());
  } else {
    // This is the fallback for local development and other environments.
    return getSdks(initializeApp(firebaseConfig));
  }
}


export function getSdks(firebaseApp: FirebaseApp) {
  return {
    firebaseApp,
    auth: getAuth(firebaseApp),
    firestore: getFirestore(firebaseApp)
  };
}

